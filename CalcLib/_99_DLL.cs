using System;

namespace CalcLib
{
    public class IntCalc
    {
        public static int Add(int a, int b) { return a + b; }
        public static int Sub(int a, int b) { return a - b; }
        public static int Mul(int a, int b) { return a * b; }
        public static int Div(int a, int b) { return a / b; }
    }
}
